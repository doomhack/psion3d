u16 projectSprite(const u16 x, const u16 y, spritehit_t* hit, const f16 f_viewCos, const f16 f_viewSin)
{
	f16 f_rx = int2fp(x) - pos.x + flt2fp(0.5f);
	f16 f_ry = int2fp(y) - pos.y + flt2fp(0.5f);

	f16 f_depth =
		fpmul(f_rx, f_viewCos) +
		fpmul(f_ry, f_viewSin);

	f16 f_side;
	s16 spanx;

	if(f_depth < SPRITE_NEAR_DEPTH)
		return FALSE;

	f_side =
		-fpmul(f_rx, f_viewSin) +
		 fpmul(f_ry, f_viewCos);

	spanx = 30 + fp2int(
		fpmul(
			fpdiv(f_side, f_depth),
			int2fp(52)
		)
	);

	if(spanx < 0 || spanx >= 60)
		return FALSE;

	hit->spriteHeight = (s16)(((s32)120 << FP_BITS) / f_depth);
	hit->f_spriteDist = f_depth;
	hit->spanX = spanx;

	return TRUE;
}

void drawSprite(const spritehit_t* spriteHit)
{
	s16 height = spriteHit->spriteHeight;
	s16 width;
	s16 left;
	s16 right;
	s16 x;
	s16 xStart;
	s16 xEnd;
	s16 top;
	s16 bottom;
	s16 yStart;
	s16 yEnd;
	s16 sourceXStep;
	s16 sourceYStep;
	s16 sourceXAcc;

	if(height <= 0)
		return;

	width = height;

	if(width < 1)
		width = 1;

	left = (spriteHit->spanX << 2) + 2 - (width >> 1);
	right = left + width;
	xStart = left;
	xEnd = right;

	if(xStart < 0)
		xStart = 0;

	if(xEnd > SCREEN_WIDTH)
		xEnd = SCREEN_WIDTH;

	if(xStart >= xEnd)
		return;

	top = 80 - (height >> 1);
	bottom = top + height;
	yStart = top;
	yEnd = bottom;

	if(yStart < 0)
		yStart = 0;

	if(yEnd > SCREEN_HEIGHT)
		yEnd = SCREEN_HEIGHT;

	if(yStart >= yEnd)
		return;

	sourceXStep = (s16)(((s32)SPRITE_SIZE << SPRITE_SCALE_BITS) / width);
	sourceYStep = (s16)(((s32)SPRITE_SIZE << SPRITE_SCALE_BITS) / height);
	sourceXAcc = (s16)((s32)(xStart - left) * sourceXStep);

	for(x = xStart; x < xEnd; x++)
	{
		u16 sourceX = sourceXAcc >> SPRITE_SCALE_BITS;
		const u8* sourceCol;
		u16 offset;
		u8 mask;
		u8 keepMask;
		s16 sourceYAcc;
		s16 y;

		sourceCol = gunner + (sourceX << 4);
		offset = (yStart << 5) + (x >> 3);
		mask = 1 << (x & 7);
		keepMask = ~mask;
		sourceYAcc = (s16)((s32)(yStart - top) * sourceYStep);

		for(y = yStart; y < yEnd; y++)
		{
			u16 sourceY = sourceYAcc >> SPRITE_SCALE_BITS;
			u8 packed;
			u8 pix;

			packed = sourceCol[sourceY >> 2];
			pix = (packed >> ((sourceY & 3) << 1)) & 3;

			switch(pix)
			{
				case SPR_GREY:
					blackBm[offset] &= keepMask;
					greyBm[offset] |= mask;
					break;
				case SPR_BLACK:
					blackBm[offset] |= mask;
					greyBm[offset] &= keepMask;
					break;
				case SPR_SHADE:
					greyBm[offset] |= mask;
					break;
			}

			sourceYAcc += sourceYStep;
			offset += SCREEN_ROW_BYTES;
		}

		sourceXAcc += sourceXStep;
	}
}
